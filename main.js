  let ganoLocal = false;
  let empate = false;
  if (golesLocal > golesVisitante) ganoLocal = true;
  else if (golesLocal == golesVisitante) empate = true;


  // console.log(`${local} ${golesLocal} | ${golesVisitante} ${visitante}`);
  let tds = document.querySelectorAll('td');
